print("VAMOS A CALCULAR EL PROMEDIO DE 4 NUMEROS!!")
cantidad_num = 4
prim = float(input("Digita el primer numero: "))
segu = float(input("Digita el Segundo numero: "))
terc = float(input("Digita el Tercer numero: "))
cuar = float(input("Digita el Cuarto numero: "))
suma = prim + segu + terc + cuar
promedio = suma / cantidad_num

print(f"La suma de estos cuarto numeros es : {suma},  y el promedio es: {promedio}")

